type MessageKey = keyof typeof messages['zh-CN']

function format(value: string, params?: Record<string, string | number>) {
  if (!params) return value
  return value.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? ''))
}

export function useLocale() {
  function setLocale(next: Locale) {
    locale.value = normalizeLocale(next)
    localStorage.setItem(STORAGE_KEY, locale.value)
  }

  function t(key: MessageKey, params?: Record<string, string | number>): string {
    return format(messages[locale.value][key] || messages['zh-CN'][key] || key, params)
  }

  const isEnglish = computed(() => locale.value === 'en')

  return { locale, setLocale, t, isEnglish }
}
